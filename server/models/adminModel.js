import mongoose from "mongoose";

//admin schema created
const adminSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phone: {
    type: Number,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

//Models created
const admin = mongoose.model("admin", adminSchema);
export default admin;